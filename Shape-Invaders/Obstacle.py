import pygame

class Block(pygame.sprite.Sprite): #small block in the obstacle
    def __init__(self, x, y):
        super().__init__()
        self.image = pygame.Surface((3, 3))
        self.image.fill((243, 216, 6))
        self.rect = self.image.get_rect(topleft = (x, y))

